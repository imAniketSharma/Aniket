var typed = $(".typed");

$(function () {
  var strings = $('.typed-items').text();
  strings = $('.typed-items').data('typed-person') + ',' + strings;
  strings = strings.split(',');

  typed.typed({
    strings: strings,
    loop: true,
    typeSpeed: 100,
    backSpeed: 50,
    backDelay: 2000
  });
});

